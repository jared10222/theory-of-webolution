<h3 id="6">How to train your brain to get into that state of focus</h3>

<p class="lead">Once you understand why your mind gets distracted, you are able to train your brain to concentrate better.</p>

<p>When you are focused, both sides of your brain are working together and are able to block out what's going on around you.</p>
<p>When your focus state is broken, it can take you up to 25 minutes to regain it. Every time it gets broken, you have to start over again.</p>

<p class="lead">How can we take control of a wandering mind for a more productive life?</p>
<ol>
	<li>Work out on what is causing the wandering in the first place;</li>
    <li>Turn off your email so you don't keep getting pinged when one comes in;</li>
    <li>It's essential to be completely aware of your thought processes and to stop any impulse to think about something else, before this happens.</li>
</ol>

<hr />
<p class="lead">Train your brain for the focus mode</p>
<pre class="prettyprint">
<div class="nocode">
#1. make the task you have to complete more relevant.

How can you do this?

Place your most important task at the top of your to do list and the least
important at the bottom of your list. Define as well a short deadline.

Once completing it by the deadline, offer yourself a reward.

<hr />
#2. When multitasking, you lose the ability to focus as distraction
becomes a habit.

By turning off all distractions and committing your attention to a single
task, you start building the muscle to be focused.

You can start small, maybe five minutes per day, and work up to larger timeframes.
If you find your mind wandering, just return to the task at hand.

<hr />
#3. In order to learn how to focus on important tasks, use entertainment as
a form of training.

Choose a good book, and read it that passionately, that you are unaware of the
world around you: that is true focus.

Whether it is a good book, a great film, or your own work, the end goal is the same:
your focus level.

It doesn't really matter what form of entertainment you use. But whatever you choose,
it must be challenging and you must be actively focused on whatever you are doing.

</div>
</pre>


