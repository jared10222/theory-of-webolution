<h3 id="5">Concentration excercises for boosting and sharpening your focus</h3>
<p class="lead">The brain exercises that you will choose have to be unfamiliar and out of your comfort zone.</p>

<pre class="prettyprint">
<div class="nocode">
#1. Count Backwards

In your mind, without saying the numbers out aloud, start counting
backwards.

Easy? Then try this, start counting again from 100, but skip the 4 digits 
in between for example 100, 96, 92 etc.
</div>
</pre>

<pre class="prettyprint">
<div class="nocode">
#2. Select an inspiring word or just a simple sound and think about it in your
mind, repeating it in your mind for 5 minutes.

Once your mind has begun to concentrate on it easily, move on to 10 minutes
of uniterrupted concentration.
</div>
</pre>

<pre class="prettyprint">
<div class="nocode">
#3. Try to suppress your thoughts for at least 5 minutes.

Start off with one minute and gradually increase the length of time. Try your best
to stay for 5 minutes without a single thought in your mind.
</div>
</pre>

<pre class="prettyprint">
<div class="nocode">
#4. Concentrate from within

Start by lying down and thoroughly relaxing your muscles. Now concentrate on your
heartbeat without paying attention to anything else.

Think about your heart and how it is pumping blood to every part of the body
</div>
</pre>

<pre class="prettyprint">
<div class="nocode">
#5. Drawing and coloring are great activities to keep the brain relaxed
and to stimulate creativity

</div>
</pre>

<pre class="prettyprint">
<div class="nocode">
#6. Concentrating on Sleep

Put a full glass of clear water on a table in your sleeping room. Sit in a chair
beside the table and gaze into the glass of water and think how calm it is. Then
picture yourself getting into just as calm a state.
</div>
</pre>

<pre class="prettyprint">
<div class="nocode">
#7. Belly Breathing

Place on hand on your stomach.

Inhale slowly through your nose, and into your abdomen to make your stomach expand
for about three seconds.

Exhale for another three seconds by slowly pushing your breath out with your
abdominal muscles.
</div>
</pre>

<pre class="prettyprint">
<div class="nocode">
#8. The Chair Exercise

Pick a straight back, comfortable chair and sit. Make sure that while sitting, you
are not moving a muscle.

Your body shouldn't move a millimeter except your chest and nostrils, for breathing.

You may not be able to do it for more than five minutes at a stretch, but practice
hard and fix a target of fifteen minutes
</div>
</pre>

<pre class="prettyprint">
<div class="nocode">
#9. Breathing in and out

Sit upright on a chair or on the ground, with your head and back in a straight line.
Press your index finger against your right nostril and breathe in gently through
your left, mentally ticking off ten seconds.

Now remove your finger and exhale through your right nostril, again taking ten 
seconds to do so. Repeat the whole procedure with the other finger and left nostril.
</div>
</pre>

<pre class="prettyprint">
<div class="nocode">
#10. Noise Cancellation

The ideal place to do this will be on a park next to the road.
Close your eyes and take a stock of all the sounds around you.

Pick a sound and concentrate on it, blocking out everything else.

After two or three minutes, switch your focus to another
sound and concentrate on it, ignoring everything else in turn.
</div>
</pre>


