<h4>Mapping (or Webbing)</h4>
<ul>
	<li>Great for visual learners</li>
    <li>Also known as webbing</li>
    <li>It is a visual brainstorm</li>
    <li>Circle in the middle with your topic written in middle</li>
    <li>Draw lines to circles with supporting ideas</li>
    <li>Around those circles you can draw lines to another layer of details.</li>
</ul>

<h4>Freewriting</h4>
<ul>
	<li>Great for a tapping into the unconscious mind</li>
    <li>Write across the page.</li>
    <li>Seems like sentences and paragraphs, BUT
    	<ul>
        	<li>No thoughts of grammar</li>
            <li>Don't even stay within line</li>
            <li>If you get stuck repeat words or day dream don't lift your pen or pencil</li>
        </ul>
    </li>
    <li>Times exercise: 1, 3, or even 10 minutes</li>
    <li>Great for more creative writing</li>
</ul>

<h4>Research, Journal, Questions</h4>
<ul>
	<li><b>Research:</b> Go online and search the topic to read the latest articles or essays. Get the tenor of conversation; then begin to notice what points you should focus on.</li>
    <li><b>Journal:</b> Take notes as you research; scribble down ideas for your own paper as they pop in to your head.</li>
    <li><b>Reporters Questions:</b> Who, What, When, Where, Why and How. Answer these questions to generate ideas.</li>
</ul>

