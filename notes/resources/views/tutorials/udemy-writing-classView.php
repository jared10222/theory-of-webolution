<h3 id="narrow">Narrowing the topic</h3>
<ul>
	<li>Given a broad topic, you need to decide what specifically you want to write about</li>
    <li>Used for all writing situations: proposals, essays, or letters. You name it.</li>
    <li>Prewriting strategies: Brainstorming, Freewriting, Mapping, Research, Journaling, or Questions.</li>
    <li>Turn your editor off for all of these idea generating strategies</li>
</ul>

