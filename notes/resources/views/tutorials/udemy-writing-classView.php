<h3 id="working-thesis">Working Thesis</h3>
<ul>
	<li><b>Topic</b> The over all most general idea of the writing.</li>
    <li><b>Controlling Idea:</b> What you specifically want to say about this general topic.
    	<ul>
        	<li>Example: Blue Cheese is a very general topic</li>
            <li>Controlling idea is: east coast people only dip wings in blue cheese.</li>
        </ul>
    </li>
    <li>Working thesis means that the thesis can still be changed or refined as you gather supporting details and learn more about the topic.</li>
</ul>

