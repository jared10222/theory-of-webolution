<h3 id="refine">Add and drop support and/or refine thesis</h3>
<ul>
	<li>Drop ideas that don't fit thesis</li>
    <li>Add ideas where there are spaces</li>
    <li>Rework thesis as the topic evolves</li>
</ul>

<hr />
<h3 id="cluster">Cluster &amp; Arrange</h3>
<ul>
	<li>Group together support that may fit in paragraphs together</li>
    <li>Use simple lists of small phrases</li>
    <li>Put numbers on each cluster to get a rough ideas of the flow and organization</li>
</ul>

<hr />
<h3 id="draft">Draft</h3>
<p class="lead">The secret to the draft is:</p>
<ul>
	<li>Quick and easy</li>
    <li>Keep moving</li>
    <li>Add a check mark where you need to return to add or fix</li>
    <li>After you simply transcribe your ideas from prewriting into rough sentences and paragraphs, take a nice long break (at least an hour or 2 doing something different)</li>
</ul>

<hr />
<h3 id="revise">Revise (Re-Vision)</h3>
<p class="lead">Take some time off so you can see your writing with fresh eyes.</p>
<ol>
	<li>Focus on content only</li>
    <li>Print it out</li>
    <li>Read it out loud pen in hand</li>
    <li>Mark in margin anything that seems weak, awkward, unfinished, off topic, or out of place.</li>
    <li>Take a moment to fix it on the computer or long hand reading the sentence out loud until it sounds right.</li>
</ol>

<hr />
<h3 id="edit">Edit</h3>
<p class="lead">When you are confident you said all you need to say and it is complete, coherent, unified, and concise now you can move to editing.</p>
<ol>
	<li>Focus on grammar, punctuation, and style.</li>
    <li>Print it out</li>
    <li>Read it out loud with pen in hand</li>
    <li>Mark in margin anything that seems possibly wrong</li>
    <li>Take a moment to fix each marking on the computer or long hand while reading the sentence out loud until it is correct.</li>
</ol>

<hr />
<h3 id="proofread">Proofread</h3>
<p class="lead">This is when you polish your writing to a shiny perfect finished document.</p>
<ol>
	<li>Focus on any mistakes that slipped by you and on style and flow. Really trust your ear here.</li>
    <li>Print it out and read it out loud with pen in hand</li>
    <li>Makr in margin anything wrong.</li>
    <li>Take a moment to fix each marking on the computer.</li>
    <li>You must commit to proofreading multiple times (4-5) spread out across a day or two.</li>
</ol>

