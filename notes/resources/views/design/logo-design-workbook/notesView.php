      <h2>2 - Identify, don't explain</h2>
      
      <p>We are identified, in good company, with names like John, Maria, or Frank. We prefer to not be called "the guy who lives on Maple Street and works at the pharmacy" or "the woman who has a beehive hairstyle and runs a trucking company". This is long-winded, confusing, and forgettable. In the same way, a logo should not literally describe the client's business; a logo is an identifier. The logo is a signpost that identifies the company and reflects its attitude and values.</p>
      
      <hr />
      <h2>3 - Understand limitations</h2>
      
      <p>Here is the bad news: A logo is not a magic lantern. It can't make a bad product successful or save a poorly managed corporation. This is the good news: A well-designed logo will always help a good product realize its full potential. Smart design, along with the power of repetition, can make an enormous impact. The logo gives direction and attitude, while the product informs the meaning.</p>
      
      <hr />
      <h2>4 - Be seductive</h2>
      
      <p>There is an enormous amount of dialog in design education and design oriented critical thinking about the irrelevance of pleasurable aesthetics. Over the past fifty years, the idea of logos as visually satisfying forms has been minimized. While this may play into fashionable cynicism, most people would prefer to be seduced by a mark than repulsed by one. The message must be the most important part of the identity's design, but the form must draw the viewer into it. Making visuals aesthetically seductive is another book, but logos are most successful when they are simple and dynamic.</p>
      
      <hr />
      <h2>5 - Make mnemonic value</h2>
      
      <p>When we deconstruct how memory is made, we find there are four critical attributes of the process: (1) We see shape and color. All our visual recognitions are based on this. Is something square and red, or round and yellow? From the way we read letterforms, to the way we identify faces, shap and color form the basis of this skill. Once the shape and color of a form have been determined, we (2) position it within our understanding of historical continuity. We ask ourselves, "Does this look contemporary, Victorian, or Medieval?" "Does this have relevance to me at this time?"</p>
      
      <p>(3)We then use the information we have from learned responses to form meaning. We are taught very specific ideas: blue is masculine and pink is feminine, a red light means "stop", a green light means "go". (4)Mnemonic value is linked seamlessly with emotional association. This is the "wild card". It is personal and difficult to predetermine. If a green car hit you when you were a child, you may have an aversion to green. If your mother wears Chanel No 5, you may feel warm (or other more complicated emotions) when seeing the Chanel logo. Being aware of and utilizing these four attributes provides the tools to produce mnemonic value.</p>
      
      <hr />
      <h2>6 - Pose a question</h2>
      <p><i>"If you can't explain the idea in one sentence over the telephone, it won't work," - Lou Danziger</i></p>
      
      <p>When we receive input from our senses, there is a question, "What is this taste?" and a response, "This is chocolate." We also do this when we watch television, listen to music, or read a book. This is part of our thinking process. The books and television programs we find the most unsatisfying are often the most predictable. If the viewer is given all the facts there is little reason for him to process the information. ALternatively, if the question is presented, and the viewer must provide an answer in his head, he will be forced to spend more time with the message and therefore become more intimate with it. There is a find line, however, between posing a question that invites a response and asking an unsolvable one. A visual solution that takes hours to interpret, or needs accompanying text will not succeed, and will soon be resigned from usage.</p>
      
      <hr />
      <h2>7 - Design for longevity</h2>
      
      <p>Every hour we are barraged with an endless array of images and ideas. our visual landscape is composed of billboards and signs, television commercials, magazine advertisements, messages on packaging, and other forms of visual communication. Almost every one of these messages is combined with a logo, but many of these have little impact and are quickly forgotten. The ideas that connect are the ideas that resonate with us emotionally. Style and trends may be enticing, but they rarely have lasting emotional resonance. Marks that date quickly result from a concentration on "formal", rather than "conceptual" ideas. The logo must be able to convey its message over a long period of time and it must be able to adapt to cultural changes. It might be exciting to design a logo that is influenced by the typeface du jour, but it will quickly become embarrassing and will need to be redesigned in later years. Marks designed with a focus on current style and trends are often outdated in a short amount of time and soon become "quaint." There are very few clients who would like to be perceived as either outdated or quaint.</p>
      
      <p><b>Considerations that impact longevity:</b></p>
      <ul>
      	<li>Is the logo a strong representation of the client, as well as their current and future goals?</li>
        <li>Is the typography based on classic forms that will stand the test of time or does it depend on a style that will ultimately cause it to be trendy and short-lived?</li>
        <li>Is the iconography culture-specific or universal? The answer could impact future use.</li>
        <li>Is the logo well-crafted enough and aesthetically appropriate to exist unchanged for years?</li>
      </ul>
      
      <hr />
      <h2>8 - Make the logo the foundation of a system</h2>
      
      <p>Like the foundation of a building, the logo is the base for all other messages. when the designer is in the process of designing a logo, it will be the only item on the computer screen. Often, when presented to a client, it will be the only item on the page. This is a mistake. The audience will never see the logo in a void. It will always be in context, accompanied by other visuals and ideas. It may be seen on business cards, on vans, and on top of buildings. If the logo is the foundation, the visual system is, to keep the construction metaphor alive, the framing of the structure. A visual system is derived from the logo. It does not copy the mark's form, but compliments it. The visual system will include guidelines for usage of color, typography, imagery, copy style, and product usage. Without these guidelines, very bad things can happen to the logo. Paryt hats could be put on it for Christmas cards, its color could be changed to something inappropriate, or it might be used as signage on the lobby floor, stepped on daily. The guidelines protect the mark and clarify the environment it occupies. This, consequently, protects the integrity of its message and the company it represents.</p>
      
      <hr />
      <h2>9 - Design for a variety of media</h2>
      
      <p>The logo should now be legible and clear on a one-color newspaper ad, a website, three-dimensional signage, and on television. Most clients will have a predisposed idea of the logo's usage. At the time of its inception they may only intend to use the mark in print. Given the constant evolution of media and information delivery system, it would be very unique for the mark to exist only in one medium over its lifespan. Once again, it is the designer's responsibility to plan for the unplanned.</p>
      
      <hr />
      <h2>10 - Be strong</h2>
      
      <p>There is a fine line between intransigence and confidence, or between uncertainty and collaboration. The design process is often subjective, with logos and identity at the core of a sense of self. A client's love of red, for example, may be irrelevant to the strategy, but rejection of that idea may become a deeply personal issue. on the other hand, the designer may fall in love with the style of a logo that is not conceptually relevant. In order to reach a solution that solves the problems with sustainability, the final logo must address the client's goals and messages. Sidestepping the emotional landmines and personal politics is one of the most challenging aspects of the design process. While every situation is different, the best solution is to maintain a clear vision and connection to the primary goal. The designer, as an outside consultant, will be able to see the larger picture without being distracted by day-to-day operations. Frequently reminding the client of the desired outcome and central message is critical.</p>
      
