      <h2 id="shape">Shape</h2>
		
      <p>A good logo will involve a shape that is appropriate and memorable. Shape is at the core of mneumonic value. Although is would be easy to say that a circle is the most successful shape for a logo, it would be untrue. There are logos made with squares, ellipses, triangles, and other unique shapes that are equally sucessful.</p>
      
      <p>It is also wrong to think that all logos must be contained within a shape. the overall form of a logo should comprise a shape. This is achieved by the letterforms and icon being constrained within a shap. Alternatively, the letterforms and icon can create an implied shape.</p>
      
      <hr />
      <h2 id="hierarchy-and-scale">Hierarchy and Scale</h2>
      <p>Myth: Everyone likes everything bigger. A logo must be able to exist as a twenty-five foot sign on a building, but it must also function on a business card. Intricate, complex, and layered forms might look incredible on the computer screen at 400 percent, but will become a jumbled mess on a CD label. Obviously, simple forms reduce best. Conversely, bad curves or sloppy kerning are only exaggerated on a fifty-foot billboard.</p>
      
      <p>The issue of hierarchy and meaning are interconnected. Is an individual product more important than the parent company? This depends on two questions. First, will it be advantageous for the client to be associated with the product? Second, will it be disadvantageous? If the connection with a product will position the client inappropriately there's a much bigger problem that needs to be addressed than just the size of the logo.</p>
      
      <hr />
      <h2 id="static-vs-changeable">Static vs. Changeable</h2>
      <p>The logo must serve as a central tool providing a cohesive voice for a wide range of applications. This does not necessitate that the logo be an intransigent, immutable object.</p>
      
      <p>Rather than allow someone else to make decisions about the changeable qualities of the logo, the designer should presuppose this scenario. Providing guidelines for motion, audio, and print is as important as choosing color. The action and reaction of the logo should reinforce the overall criteria and concept.</p>
      
      <hr />
      <h2 id="creating-a-kit">Creating a kit of parts</h2>
      <p>Identity design must be fluid. When the logo is complete, many more elements need to be created to make the logo truly usable in a variety of applications. An identity system needs to be designed that will be dynamic enough to allow for the ever-changing needs of the client. Therefore it is essential that designers create logos with flexibility in mind.</p>
      <hr />
      
      <p class="lead">CONSISTENCY OF CONCEPT</p>
      <p>It is important that the identity system functions as a cohesive group of visual and verbal elements that serve to continually identify the client to its target audience(s). Consistency is central to effective branding. This does not preclude creativity. An identity system will fail if it is predictable and lifeless. Power, clarity, and freshness must accompany consistency.</p>
      <hr />
      
      <p class="lead">CLARITY OF MESSAGE</p>
      <p>The role of the identity system is to provide a visible and obvious shorthand that supports the intended image of the client. To achieve this, every graphic element within the identiy system must clearly support the logo.</p>
      <hr />
      
      <p class="lead">ACCOMMODATING TO THE CLIENT</p>
      <p>When the system is developed, the designer must understand who wil use it and how they will use it. With this information, the designer will be able to create a system that is graphically appropriate, and cable of both accomodating the requirements and reflecting the personality of the client. The designer must create a system that can be customized within the constraints of the client's needs</p>
      <hr />
      
      <p class="lead">FLEXIBILITY FOR USERS</p>
      <p>The identity system must allow for an individual user's own modifications. As much as the original designer may not wish it, other creatives will use the identity system. Plan for enough variables in the system to keep the identity fresh.</p>
      
      <hr />
      
