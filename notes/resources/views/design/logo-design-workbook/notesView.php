      <h2 id="image-iconography">Image/Iconography</h2>
      
      <p>When we talk about iconography here, we are discussing its role in the context of logo development. Icons are loaded. They can be very powerful and convey a Large amount of information quickly. They can, conversely, be oblique and neutral, allowing for a broad range of meaning. The style of execution impacts the tone and meaning. An icon created for a logo does not need to be a hard-edged, flat drawing of an abstract shape. While direct illustration of the subject matter is a mistake, various representational techniques can be utilized</p>
      
      <p>An apple acts as both a symbol of New York and of education. This may seem like a cliche, but cliches are, intrinsically, very recognizable. Such symbols shold not be disregarded but, rather, presented in a fresh form. The iconography should engage the view. Seeing recognizable signs in a unique form is a good way to achieve this goal.</p>
      
      <p class="lead">Types of Icon</p>
      <dl>
      	<dt>Diagrammatic</dt>
        <dd>Icons are simple representations of the structure of the subject matter. The process of thinking is conveyed by an asterisk in the Spark logo.</dd>
        
        <dt>Metaphoric</dt>
        <dd>Icons are based on conceptual relationships. A hat for a talent agency communicates trust, honesty, and old-fashioned values.</dd>
        
        <dt>Symbolic</dt>
        <dd>Icons are abstract images that have no clear relationship to the subject. Their only connection is their proximity to the subject. A nonrepresentational mark next to the name of a shoppping center is a symbol.</dd>
      </dl>
      
