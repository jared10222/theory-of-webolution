      <h2>1 - Answer who, what, why?</h2>
      
      <p>Before anything begins, the most basic questions that must be asked and answered are <b>"Who is the client?" "Who is the audience?" "What is needed?"</b> A logo should grow organically from the answers to these questions. Rather than imposing an idea onto the problem, the problem should dictate the solution. This is a statement repeated by every design teacher. Unfortunately, it is often ignored or misunderstood. It does not mean the whe whims of the client should be obediently followed. It does not mean that the designer's vision should be sublimated. <b>It does mean taht as much information as possible should be gathered, criteria developed, and creative work created, through the filter of the designer.</b></p>
      
      <p>Who is the client? In the simplest terms , this addresses the company's values, attitudes, and goals. Who is the audience? This may be answered demographically--women eighteen through thirty-four, or psychographically--athletic men who love adventure. And, more specifically, who makes the final decision on this logo?</p>
      
      <p>It is human nature to focus on our current needs; it is the designer's job to presuppose future needs. (What does the client want to be in the next year, five years, and ten years? long-term goals?)</p>
      
