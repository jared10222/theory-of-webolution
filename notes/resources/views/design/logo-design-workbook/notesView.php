<h1 id="implementing-logos">Implementing Logos</h1>
<hr />
<h2>Rollout: process and best practices</h2>

<p>Now that the logo is created, an identity system is designed, and everything is approved by the client, the next step is rollout. Rollout means actually putting the logo to use in real-world scenarios. In order to prepare for these various applications of the logo, designers create identity standards manuals. This allows the original designer to visualize all the information needed to implement the logo successfully. It requires thinking through every possible use of the logo, and providing specific guidelines for those uses.</p>

<p>Since the word design means to formulate a plan, creating a road map in the form of a standards manual is a key step in the process of designing a logo.</p>

<p>Standards manuals or guidelines allow identity systems to be managed properly--because they serve as the ultimate resource for consistent application of the logo throughout all communication and visual materials required by the client.</p>

<p>Manuals must be created to be useful to the widest possible group of logo users.</p>

<hr />
<h2 id="anatomy-of-a-standards-manual">Anatomy of a Standards Manual</h2>
<p class="lead">Graphic standards manuals can be slim and concise or multipaged behemoths, depending on the personalilty and needs of the client. Whether they are printed and bound pieces or whether they exist only on the client's intranet, identity guidelines are the key to consistent use of a logo.</p>

